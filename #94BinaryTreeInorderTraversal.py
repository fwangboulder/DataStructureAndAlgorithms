# Definition for a binary tree node.
# class TreeNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None


class Solution(object):

    def inorderTraversal(self, root):
        """
        :type root: TreeNode
        :rtype: List[int]
        """

        if root is None:
            return []
        r = []
        st = []
        node = root
        st.append(root)
        while st:
            while node and node.left:
                st.append(node.left)
                node = node.left
            node = st.pop()
            r.append(node.val)
            node = node.right
            if node:
                st.append(node)
        return r
